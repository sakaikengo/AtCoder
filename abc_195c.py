#下から3桁ごとにコンマで区切って表示

#1以上N以下の整数を書く時コンマは何回で書かれるか
#1 <= N <= 10 ** 15

N = int(input())

ans = 0

#Nが10 ** 3以上の時
if N >= 1000:
    ans += N - 999
#Nが10 ** 6以上の時
if N >= 1000000:
    ans += N - 999999
#Nが10 ** 9以上の時
if N >= 1000000000:
    ans += N - 999999999
#Nが10 ** 12以上の時
if N >= 1000000000000:
    ans += N - 999999999999
#Nが10 ** 15以上の時
if N >= 1000000000000000:
    ans += N - 999999999999999

print(ans)